main() {
  //字符串定义方式
  var str1 = 'this is tr1';
  var str2 = 'this is tr2';
  print(str1);
  print(str2);

  String str3 = 'this is tr3';
  print(str3);
  String str4 = '''
  this is str4
  this is str4
  this is str4
  ''';

  ///可以写多行字符 三个" 也可以
  print(str4);
  //字符串拼接

  String stra = '你好';
  String strb = 'dart';

  print('$stra,$strb');
  print(stra + ',' + strb);
}
